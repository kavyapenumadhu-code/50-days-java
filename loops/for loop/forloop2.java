public class forloop2 {
    public static void main(String[] args) {
        for(int i=1;i<=2;i++){
            System.out.println(i+"start");
            for(int j=1;j<=2;j++){
                System.out.println(j);
                System.out.println(i);
            }
            System.out.println(i+"end");
        }
    }
}
