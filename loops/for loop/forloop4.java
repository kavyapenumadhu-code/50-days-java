// Write a program to print the multiplication table of a number entered by the user (e.g., for 5: 5 x 1 = 5, 5 x 2 = 10, ... up to 5 x 10 = 50).
public class forloop4 {
    public static void main(String[] args) {
        int n=5;
        for(int i=1;i<=10;i++){
            System.out.println(n+" * "+i+" = "+(n*i));
        }
    }
}
