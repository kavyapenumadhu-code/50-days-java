// write a java program to check whether the given number is divisible by 7 and 8
public class ifelse2 {
    public static void main(String[] args) {
        int i=56;
        if (i%7==0&&i%8==0){
            System.out.println(i+" is divisible by 7 and 8");
        }
        else{
            System.out.println(i+" is not divisible by 7 and 8");

        }
    }
}
